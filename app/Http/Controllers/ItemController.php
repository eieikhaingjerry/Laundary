    {
        $adminbank = Bank::where('id', 1)->first();
        $userbank = Bank::where('user_id', Auth::user()->id)->first();
        
        DB::table('banks')
        ->where('user_id', Auth::user()->id)
        ->update(['balance' => (($userbank->balance)-($request->finaltotal))]);

        DB::table('banks')
        ->where('id', 1)
        ->update(['balance' => (($adminbank->balance)+($request->finaltotal))]);

        return view("welcome");
    }

