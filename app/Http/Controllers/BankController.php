    {
        $bank = new Bank();
        $bank->account = $request->cardno;
        $bank->cvv = $request->cvv;
        $bank->expired = $request->exp;
        $bank->balance = 1000000000;
        $bank->user_id = Auth::user()->id;
        $bank->save();

        return view("welcome");
    }

